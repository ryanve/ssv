!function(api) {
  function expect(actual, correct) {
    if (actual !== correct) throw new Error(actual + ' should be ' + correct)
  }

  expect(api.split, api.parse)
  expect(api.split('').length, 0)
  expect(api.split(' ').length, 0)
  expect(api.split('mark').join('-'), 'mark')
  expect(api.split('mark tom').join('-'), 'mark-tom')
  expect(api.split(' mark tom ').join('-'), 'mark-tom')
  console.log('#split tests passed')

  expect(api.compact(''), '')
  expect(api.compact('    '), '')
  expect(api.compact('  mark  tom  travis  '), 'mark tom travis')
  expect(api.compact('\n mark \r tom \t travis \n\r\t'), 'mark tom travis')
  console.log('#compact tests passed')

  expect(api.any('', ''), false)
  expect(api.any('', ' '), false)
  expect(api.any(' ', ' '), false)
  expect(api.any('mark tom', ''), false)
  expect(api.any('mark tom', ' '), false)
  expect(api.any('mark tom', 'tom'), true)
  expect(api.any('mark tom', 'mark'), true)
  expect(api.any('mark tom', 'travis'), false)
  expect(api.any('  mark   tom  ', 'mark  tom'), true)
  expect(api.any('  mark   tom  ', 'mark scott'), true)
  expect(api.any('  mark   tom ', 'matt travis'), false)
  expect(api.any('  mark   tom  mark', 'travis tom'), true)
  console.log('#any tests passed')

  expect(api.slice(''), '')
  expect(api.slice('', 5), '')
  expect(api.slice('', -1), '')
  expect(api.slice('mark tom scott', 0), 'mark tom scott')
  expect(api.slice('mark tom scott', -1), 'scott')
  expect(api.slice('mark tom scott', -2), 'tom scott')
  expect(api.slice('mark tom scott', 0, 1), 'mark')
  expect(api.slice('mark tom scott', 1, 2), 'tom')
  expect(api.slice('mark tom scott', 1, 3), 'tom scott')
  expect(api.slice('mark tom scott', 0, -2), 'mark')
  expect(api.slice('mark tom scott', 0, -1), 'mark tom')
  expect(api.slice('mark tom scott', 1, -1), 'tom')
  console.log('#slice tests passed')

  expect(api.concat('', ''), '')
  expect(api.concat('', 'mark'), 'mark')
  expect(api.concat('mark tom', 'travis'), 'mark tom travis')
  expect(api.concat('mark tom', 'travis matt'), 'mark tom travis matt')
  expect(api.concat('mark tom', 'mark'), 'mark tom mark')
  expect(api.concat('  mark  tom  ', '  travis  matt '), 'mark tom travis matt')
  expect(api.concat('  mark  tom  ', 'mark'), 'mark tom mark')
  console.log('#concat tests passed')

  expect(api.uniq(''), '')
  expect(api.uniq('mark tom mark'), 'mark tom')
  expect(api.uniq('mark tom mark mark tom tom mark'), 'mark tom')
  expect(api.uniq('mark tom Mark'), 'mark tom Mark')
  console.log('#uniq tests passed')

  expect(api.union('', ''), '')
  expect(api.union('', 'mark'), 'mark')
  expect(api.union('mark tom', 'travis'), 'mark tom travis')
  expect(api.union('mark tom', 'travis matt'), 'mark tom travis matt')
  expect(api.union('mark tom', 'mark'), 'mark tom')
  expect(api.union('  mark  tom  ', '  travis  matt '), 'mark tom travis matt')
  expect(api.union('  mark  tom  ', 'mark'), 'mark tom')
  expect(api.union('mark mark mark', ''), 'mark')
  expect(api.union('mark mark mark', 'tom'), 'mark tom')
  console.log('#union tests passed')

  expect(api.diff('', ''), '')
  expect(api.diff('', 'mark'), '')
  expect(api.diff('mark tom', 'travis'), 'mark tom')
  expect(api.diff('mark tom', 'travis tom'), 'mark')
  expect(api.diff('mark tom', 'mark'), 'tom')
  expect(api.diff('  mark tom  ', ' travis  tom '), 'mark')
  expect(api.diff('  mark tom tom', 'mark mark'), 'tom tom')
  expect(api.diff('mark mark mark', ''), 'mark mark mark')
  expect(api.diff('mark mark', 'tom tom'), 'mark mark')
  expect(api.diff('mark tom travis matt', 'tom matt'), 'mark travis')
  console.log('#diff tests passed')

  console.log('All tests passed =)')
}(require('./'));
